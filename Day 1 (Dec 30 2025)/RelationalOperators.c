#include<stdio.h>

int main() {
    int a= 10, b=5;

    printf("a > b: %d\n", a > b);
    printf("a < b: %d\n", a < b);
    printf("a == b: %d\n", a == b);
    printf("a != b: %d\n", a != b);
    printf("a >= b: %d\n", a >= b);
    printf("a <= b: %d\n", a <= b);

    return 0;
}// 1 indicates true and 0 indicates false
// = is for assignment
// == is for comparison