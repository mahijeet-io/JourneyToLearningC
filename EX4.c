//to see what happens when we put main() from 0 to another number like 5
#include <stdio.h>
int main() {
    printf("i started my coding journey today");
    return 5;
}
/*The Short Conclusion
For the User (You): The program Succeeded. It printed the text you wanted.

For the Operating System (Windows): The program Failed. It reported "Error Code 5" upon exiting.

The Golden Rule: Always use return 0 to say "Success." Use other numbers only to say "Failure." */
